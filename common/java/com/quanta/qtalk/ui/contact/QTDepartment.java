package com.quanta.qtalk.ui.contact;

public class QTDepartment {
	String dep_id = "";
	String dep_name = "";
	public void setDepID(String id)
	{
		this.dep_id = id;
	}
	public String getDepID()
	{
		return this.dep_id;
	}
	public void setDepName(String name)
	{
		this.dep_name = name;
	}
	public String getDepName()
	{
		return this.dep_name;
	}
}
